class Gallery extends Component {
  state = {
    thumbnailUrlValue: imagesList[0],
  }

  stateChange = id => {
    const filterObject = imagesList.filter(eachItem => eachItem.id === id)
    console.log(filterObject)
    this.setState({
      thumbnailUrlValue: filterObject[0],
    })
  }

  render() {
    const {thumbnailUrlValue} = this.state
    const {imageUrl, imageAltText, id} = thumbnailUrlValue
    let ActiveThumbnile = ''
    return (
      <div className="mainPage">
        <img src={imageUrl} alt={imageAltText} />

        <h1>Nature Photography</h1>
        <p>Nature Photography by Rahul</p>

        <ul className="imageCard">
          {imagesList.map(eachItem => {
            ActiveThumbnile = eachItem.id !== id ? 'activeStyle' : ''
            return (
              <ThumbnailItem
                eachItem={eachItem}
                key={eachItem.id}
                stateChange={this.stateChange}
                ActiveThumbnile={ActiveThumbnile}
              />
            )
          })}
        </ul>
      </div>
    )
  }
}

export default Gallery
